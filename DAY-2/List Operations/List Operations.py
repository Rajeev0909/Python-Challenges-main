# List Operations
numbers = [10, 20, 30, 40, 50]

print("Original List:", numbers)
print("First Element:", numbers[0])
print("Last Element:", numbers[-1])
print("Slice:", numbers[1:4])  # Extract [20, 30, 40]
numbers.append(60)
print("After Append:", numbers)
numbers.remove(20)
print("After Remove:", numbers)
